package parsing;

/**
 *
 * @author dema0614
 */
public class Method {

    private String name;
    private String body;

    public Method(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

}
